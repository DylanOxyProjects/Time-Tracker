
toggleDeleteButtonOn = function() {
    var deleteBTN = document.getElementById("deleteActivity");
    var insertBTN = document.getElementById("insertTimeDiv");


    deleteBTN.style.display = "block";
    insertBTN.style.display = "block";

}

toggleDeleteButtonOff = function(){
    var deleteBTN = document.getElementById("deleteActivity");
    var insertBTN = document.getElementById("insertTimeDiv");

    deleteBTN.style.display = "none";
    insertBTN.style.display = "none";

}


